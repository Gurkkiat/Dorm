-- RLS Policies (Optional - enable if RLS is on)
ALTER TABLE meter_reading ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Enable read access for all users" ON meter_reading FOR SELECT USING (true);
CREATE POLICY "Enable insert for authenticated users only" ON meter_reading FOR INSERT WITH CHECK (auth.role() = 'authenticated' OR auth.role() = 'anon');
CREATE POLICY "Enable update for authenticated users only" ON meter_reading FOR UPDATE USING (auth.role() = 'authenticated' OR auth.role() = 'anon');
CREATE POLICY "Enable delete for authenticated users only" ON meter_reading FOR DELETE USING (auth.role() = 'authenticated' OR auth.role() = 'anon');
