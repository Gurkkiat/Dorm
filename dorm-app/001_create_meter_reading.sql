-- Create meter_reading table
CREATE TABLE IF NOT EXISTS meter_reading (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    contract_id BIGINT REFERENCES contract(id) ON DELETE CASCADE,
    room_id BIGINT REFERENCES room(id) ON DELETE CASCADE, -- Optional but helpful for direct access
    prev_water FLOAT NOT NULL DEFAULT 0,
    current_water FLOAT NOT NULL DEFAULT 0,
    prev_electricity FLOAT NOT NULL DEFAULT 0,
    current_electricity FLOAT NOT NULL DEFAULT 0,
    reading_date TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Add Reference to Room if needed (Invoice usually links Contract)
